import 'package:flutter/material.dart';
import 'package:flutter_test/flutter_test.dart';
import 'package:inventory_app/components/topbodysection.dart';
import 'package:inventory_app/pages/adding/addhomebodypage.dart';

void main() {
  group('CenterAddHomePage', () {
    test('Tworzenie obiektu', () async {
      const locationTest = Location.center;
      const sizeTest = Size(250, 100);

      final CAHPTest =
      CenterAddHomePage(size: sizeTest, location: locationTest);

      expect(CAHPTest.location, locationTest);
      expect(CAHPTest.size, sizeTest);
    });
    test('Wymiary obiektu', () async {
      const locationTest = Location.center;
      const sizeTest = Size(250, 100);

      final CAHPTest =
      CenterAddHomePage(size: sizeTest, location: locationTest);

      expect(CAHPTest.size.aspectRatio, sizeTest.aspectRatio);
      expect(CAHPTest.size.height, sizeTest.height);
      expect(CAHPTest.size.width, sizeTest.width);
    });
    test('Lokalizacja obiektu', () async {
      const locationTest = Location.center;
      const sizeTest = Size(250, 100);

      final CAHPTest =
      CenterAddHomePage(size: sizeTest, location: locationTest);

      expect(CAHPTest.location.toString(), locationTest.toString());
      expect(CAHPTest.location.index, locationTest.index);
      expect(CAHPTest.location.name, locationTest.name);
      expect(CAHPTest.location.hashCode, locationTest.hashCode);
    });
    test('Zmiana zaokrÄ…glenia', () async {
      const locationTest = Location.center;
      const sizeTest = Size(250, 100);

      final CAHPTest =
      CenterAddHomePage(size: sizeTest, location: locationTest);

      CAHPTest.roundness = 50;

      expect(CAHPTest.roundness, 50);
      expect(CAHPTest.roundness.compareTo(75), 50.compareTo(75));
    });
  });
}